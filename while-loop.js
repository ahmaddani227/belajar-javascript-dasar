/*
            #WHILE LOOP
                -While loop adalah versi perulangan yang lebih sederhana dibanding for loop
                -Di while loop, hanya terdapat kondisi perulangan, tanpa ada Init Statement dan Post Satement
        */

let counter = 1;
while (counter <= 10) {
  document.writeln(`<p>Perulangan ke ${counter}</p>`);
  counter++;
}
