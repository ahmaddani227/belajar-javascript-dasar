/*
            -JavaScript memiliki fitur untuk melakukan Logging bernama Console.
            -Logging adalah mekanisme yang biasa dilakukan oleh programmer untuk menampilkan informasi dari aplikasi yang sedang berjalan, tanpa harus mengganggu alur kerja aplikasi dan interaksi user.
            -Untuk melakukan ini, kita bisa mengguankan console di JavaScript.
            -Untuk menggunakan Console, kita cukup gunakan perintah console di kode JavaScript.
            
            PROSES DEBUGGING
            -------------------------------------------------
            Console Method

            console.info(...) = console.log(...)
            Direkomendasikan memakai info biar tau kalo ini adlah informasi.

            console.info(...)   Memberitahu Informasi
            console.warn(...)   Memberitau Informasi Peringatan 
            console.error(...)  Memberitahu Informasi Error       
            console.table(...)  Memberitahu Informasi dalam bentuk table
        */

console.debug("Debug");
console.info("Hello World");
console.log("Hello World");
console.warn("Ini Peringatan");
